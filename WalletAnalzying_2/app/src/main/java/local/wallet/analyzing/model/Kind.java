package local.wallet.analyzing.model;

public class Kind {

    private long id;

    private String name;

    public Kind() {
    }

    public Kind(int id, String name) {
        super();
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
